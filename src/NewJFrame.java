    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        // submit deret aritmatika
         try {
            // Get input values
            int nilaiAwal = Integer.parseInt(tfNilai3.getText().trim());
            int beda = Integer.parseInt(tfBeda1.getText().trim());
            int jumlahElemen = Integer.parseInt(tfElemen1.getText().trim());

            // Validate input
            if (jumlahElemen <= 0) {
                tfHasilnya1.setText("Jumlah elemen harus lebih dari 0.");
                return;
            }

            // Calculate arithmetic series
            StringBuilder hasil = new StringBuilder();
            int sum = 0;
            for (int i = 0; i < jumlahElemen; i++) {
                int nilai = nilaiAwal + i * beda;
                sum += nilai;
                hasil.append(nilai).append(i < jumlahElemen - 1 ? ", " : "");
            }

            // Display result
            tfHasilnya1.setText("" + hasil.toString() + "\nJumlah: " + sum);
        } catch (NumberFormatException e) {
            tfHasilnya1.setText("Input tidak valid. Masukkan angka yang benar.");
        }
    }//GEN-LAST:event_jButton14ActionPerformed

