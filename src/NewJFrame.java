    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // submit button
        String input = tfAngka.getText();

        try {
            //split input into an array of strings
            String[] stringNumbers = input.split(",");
            //convert string array to integer
            int[] numbers = new int[stringNumbers.length];
            for (int i =0; i < stringNumbers.length; i++) {
                numbers[i] = Integer.parseInt(stringNumbers[i].trim());
            }
            // Perform bubble sort
            bubbleSort(numbers);

            // Build sorted number string
            StringBuilder sortedNumbers = new StringBuilder();
            for (int i = 0; i < numbers.length; i++) {
                sortedNumbers.append(numbers[i]);
                if (i < numbers.length - 1) {
                    sortedNumbers.append(", ");
                }
            }

            // Display sorted numbers
            tfHasil.setText(sortedNumbers.toString());

            // menampilkan total angka yang sudah di sort
            tfTotalAngka.setText(String.valueOf(numbers.length));

        } catch (NumberFormatException e) {
            tfHasil.setText("Input invalid. Masukkan angka yang dipisahkan dengan koma.");
            tfTotalAngka.setText("");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

